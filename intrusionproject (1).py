def load_and_predict(dataset_name, X_new):
    """
    Load best saved model + scaler from Drive and run inference.

    Usage:
        X_new  = raw (unscaled) feature array, shape (n_samples, n_features)
        preds  = load_and_predict("NSL_KDD", X_new)
        # preds: array of 0 (Normal) or 1 (Attack)
    """
    model_path  = os.path.join(DRIVE_DIR, f"{dataset_name}_cnn_best.keras")
    scaler_path = os.path.join(DRIVE_DIR, f"{dataset_name}_scaler.pkl")

    if not os.path.exists(model_path):
        raise FileNotFoundError(f"No saved model found for {dataset_name}. Train first.")

    model  = tf.keras.models.load_model(model_path)
    scaler = joblib.load(scaler_path)

    X_scaled = scaler.transform(X_new)[..., np.newaxis]
    probs    = model.predict(X_scaled, verbose=0)
    preds    = np.argmax(probs, axis=1)
    return preds  # 0 = Normal, 1 = Attack

