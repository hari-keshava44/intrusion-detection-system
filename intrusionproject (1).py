def ctgan_balance(X, y, dataset_name, n_aug_samples=5000):
    """
    Balance minority class using CTGAN synthetic data generation.
    Saves augmented dataset to Drive — skips on subsequent runs.
    """
    aug_path = os.path.join(DRIVE_DIR, f"{dataset_name}_ctgan_augmented.csv")

    if os.path.exists(aug_path):
        print(f"[✓] CTGAN augmented data for {dataset_name} loaded from Drive")
        aug = pd.read_csv(aug_path)
        X_aug = aug.drop('label', axis=1).values.astype(np.float32)
        y_aug = aug['label'].values.astype(int)
        return X_aug, y_aug

    print(f"\n[CTGAN] Generating synthetic minority samples for {dataset_name} …")
    unique, counts = np.unique(y, return_counts=True)
    minority_class = unique[np.argmin(counts)]
    majority_class = unique[np.argmax(counts)]

    print(f"  Class distribution — {dict(zip(unique, counts))}")
    print(f"  Minority class: {minority_class} ({counts.min()} samples)")

    minority_idx = np.where(y == minority_class)[0]
    X_min = X[minority_idx]

    # Cap at 3000 samples for faster CTGAN training
    sample_size = min(len(X_min), 3000)
    X_sample = X_min[:sample_size]

    feat_cols = [f"f{i}" for i in range(X_sample.shape[1])]
    df_min = pd.DataFrame(X_sample, columns=feat_cols)
    df_min['label'] = minority_class

    metadata = SingleTableMetadata()
    metadata.detect_from_dataframe(df_min)

    synthesizer = CTGANSynthesizer(metadata, epochs=100, verbose=True)
    synthesizer.fit(df_min)
    synthetic = synthesizer.sample(num_rows=n_aug_samples)
    synthetic['label'] = minority_class

    # Combine original + synthetic
    df_orig = pd.DataFrame(X, columns=feat_cols)
    df_orig['label'] = y
    df_combined = pd.concat([df_orig, synthetic], ignore_index=True)
    df_combined.to_csv(aug_path, index=False)

    print(f"[✓] Augmented dataset saved → {aug_path}")
    new_counts = df_combined['label'].value_counts().to_dict()
    print(f"  New class distribution: {new_counts}")

    X_aug = df_combined.drop('label', axis=1).values.astype(np.float32)
    y_aug = df_combined['label'].values.astype(int)
    return X_aug, y_aug

