def preprocess_nsl(train_df, test_df):
    """Merge, encode, and return X, y for NSL-KDD (binary)."""
    df = pd.concat([train_df, test_df], ignore_index=True)

    # Assign column names if file was loaded without header
    if df.columns[0] != 'duration':
        df.columns = NSL_COLS

    # Drop difficulty column if present
    df.drop(columns=['difficulty'], inplace=True, errors='ignore')

    # Binary label: normal=0, attack=1
    df['label'] = df['label'].apply(
        lambda x: 0 if str(x).strip().lower() == 'normal' else 1
    )

    # Encode categorical columns
    cat_cols = ['protocol_type', 'service', 'flag']
    for col in cat_cols:
        le = LabelEncoder()
        df[col] = le.fit_transform(df[col].astype(str))

    df.replace([np.inf, -np.inf], np.nan, inplace=True)
    df.fillna(0, inplace=True)

    X = df.drop('label', axis=1).values.astype(np.float32)
    y = df['label'].values.astype(int)
    return X, y


def preprocess_unsw(df):
    """Clean, encode, and return X, y for UNSW-NB15 (binary)."""
    df = df.copy()

    # Drop non-feature columns
    drop_cols = ['id', 'attack_cat', 'srcip', 'dstip', 'Stime', 'Ltime']
    df.drop(columns=[c for c in drop_cols if c in df.columns], inplace=True)

    # Detect label column (could be 'label' or 'Label')
    label_col = next((c for c in df.columns if c.lower() == 'label'), None)
    if label_col is None:
        raise ValueError("Label column not found in UNSW-NB15 dataset!")

    # Binary label
    df[label_col] = pd.to_numeric(df[label_col], errors='coerce').fillna(0).astype(int)
    df[label_col] = df[label_col].apply(lambda x: 0 if x == 0 else 1)

    # Encode remaining object columns
    for col in df.select_dtypes(include='object').columns:
        le = LabelEncoder()
        df[col] = le.fit_transform(df[col].astype(str))

    df.replace([np.inf, -np.inf], np.nan, inplace=True)
    df.fillna(0, inplace=True)

    X = df.drop(label_col, axis=1).values.astype(np.float32)
    y = df[label_col].values.astype(int)
    return X, y

