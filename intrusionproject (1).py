def train_evaluate(X, y, dataset_name, epochs=30, batch_size=256):
    """
    Full training pipeline with evaluation and plots.
    Best model is saved to Drive automatically.
    """
    print(f"\n{'='*58}")
    print(f"  TRAINING: {dataset_name}")
    print(f"{'='*58}")

    # Scale features
    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X)

    # Save scaler for inference
    scaler_path = os.path.join(DRIVE_DIR, f"{dataset_name}_scaler.pkl")
    joblib.dump(scaler, scaler_path)

    # Train/test split
    X_train, X_test, y_train, y_test = train_test_split(
        X_scaled, y, test_size=0.2, random_state=SEED, stratify=y
    )

    # Reshape for CNN: (samples, features, 1)
    X_train_cnn = X_train[..., np.newaxis]
    X_test_cnn  = X_test[..., np.newaxis]

    # Class weights for any residual imbalance
    cw = compute_class_weight('balanced', classes=np.unique(y_train), y=y_train)
    class_weight = dict(enumerate(cw))
    print(f"  Class weights: {class_weight}")

    # Build model
    model = build_cnn(input_dim=X_train_cnn.shape[1])
    model.summary()

    # Callbacks
    model_path = os.path.join(DRIVE_DIR, f"{dataset_name}_cnn_best.keras")
    cb_list = [
        callbacks.ModelCheckpoint(
            model_path, save_best_only=True,
            monitor='val_accuracy', verbose=1
        ),
        callbacks.EarlyStopping(
            patience=7, restore_best_weights=True,
            monitor='val_loss', verbose=1
        ),
        callbacks.ReduceLROnPlateau(
            patience=3, factor=0.5, min_lr=1e-6, verbose=1
        )
    ]

    # Train
    t0 = time.time()
    history = model.fit(
        X_train_cnn, y_train,
        validation_split=0.15,
        epochs=epochs,
        batch_size=batch_size,
        class_weight=class_weight,
        callbacks=cb_list,
        verbose=1
    )
    elapsed = time.time() - t0
    print(f"\n[✓] Training completed in {elapsed:.1f}s")

    # ── PREDICTIONS ──────────────────────────────────────────
    y_pred_prob = model.predict(X_test_cnn, verbose=0)
    y_pred      = np.argmax(y_pred_prob, axis=1)

    # ── METRICS ──────────────────────────────────────────────
    acc  = accuracy_score(y_test, y_pred)
    f1   = f1_score(y_test, y_pred, average='weighted')
    prec = precision_score(y_test, y_pred, average='weighted', zero_division=0)
    rec  = recall_score(y_test, y_pred, average='weighted')
    auc  = roc_auc_score(y_test, y_pred_prob[:, 1]) \
           if len(np.unique(y_test)) == 2 else None

    print(f"\n{'─'*40}")
    print(f"  {dataset_name} — Final Results")
    print(f"{'─'*40}")
    print(f"  Accuracy  : {acc:.4f}  ({acc*100:.2f}%)")
    print(f"  F1-Score  : {f1:.4f}")
    print(f"  Precision : {prec:.4f}")
    print(f"  Recall    : {rec:.4f}")
    if auc:
        print(f"  ROC-AUC   : {auc:.4f}")
    print(f"{'─'*40}")
    print(classification_report(
        y_test, y_pred,
        target_names=['Normal', 'Attack'],
        digits=4
    ))

    # ── PLOT 1: Training curves + Confusion Matrix ────────────
    fig, axes = plt.subplots(1, 3, figsize=(20, 5))
    fig.suptitle(f"{dataset_name}  |  CNN Evaluation", fontsize=15, fontweight='bold')

    # Accuracy curve
    axes[0].plot(history.history['accuracy'],     label='Train', linewidth=2)
    axes[0].plot(history.history['val_accuracy'], label='Val',   linewidth=2)
    axes[0].set_title('Accuracy', fontsize=12)
    axes[0].set_xlabel('Epoch'); axes[0].set_ylabel('Accuracy')
    axes[0].legend(); axes[0].grid(True, alpha=0.3)

    # Loss curve
    axes[1].plot(history.history['loss'],     label='Train', linewidth=2, color='tomato')
    axes[1].plot(history.history['val_loss'], label='Val',   linewidth=2, color='coral')
    axes[1].set_title('Loss', fontsize=12)
    axes[1].set_xlabel('Epoch'); axes[1].set_ylabel('Loss')
    axes[1].legend(); axes[1].grid(True, alpha=0.3)

    # Confusion Matrix
    cm = confusion_matrix(y_test, y_pred)
    sns.heatmap(
        cm, annot=True, fmt='d', cmap='Blues', ax=axes[2],
        xticklabels=['Normal', 'Attack'],
        yticklabels=['Normal', 'Attack'],
        linewidths=0.5
    )
    axes[2].set_title('Confusion Matrix', fontsize=12)
    axes[2].set_ylabel('True Label')
    axes[2].set_xlabel('Predicted Label')

    plt.tight_layout()
    plot_path = os.path.join(DRIVE_DIR, f"{dataset_name}_evaluation.png")
    plt.savefig(plot_path, dpi=150, bbox_inches='tight')
    plt.show()
    print(f"[✓] Evaluation plot saved → {plot_path}")

    # ── PLOT 2: ROC Curve ─────────────────────────────────────
    if auc:
        fpr, tpr, _ = roc_curve(y_test, y_pred_prob[:, 1])
        plt.figure(figsize=(6, 5))
        plt.plot(fpr, tpr, color='darkorange', linewidth=2,
                 label=f'ROC Curve (AUC = {auc:.4f})')
        plt.plot([0, 1], [0, 1], 'k--', linewidth=1)
        plt.fill_between(fpr, tpr, alpha=0.1, color='darkorange')
        plt.title(f'{dataset_name} — ROC Curve', fontsize=13)
        plt.xlabel('False Positive Rate')
        plt.ylabel('True Positive Rate')
        plt.legend(loc='lower right')
        plt.grid(True, alpha=0.3)
        roc_path = os.path.join(DRIVE_DIR, f"{dataset_name}_roc.png")
        plt.savefig(roc_path, dpi=150, bbox_inches='tight')
        plt.show()
        print(f"[✓] ROC curve saved → {roc_path}")

    return model, scaler, history

